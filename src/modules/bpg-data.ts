/** Clé BPG pour un rang (ex: 'C-11-3') */
export function bpgDeRang(rang: number): BpgId | null {
  const i = _findIndex(rang);
  return i >= 0 ? _indexToId(i) : null;
}

/** Info BPG complète pour un rang */
export function infoBpgDeRang(rang: number): BpgInfo | null {
  const i = _findIndex(rang);
  return i >= 0 ? _indexToInfo(i) : null;
}

/** Partenaires structurels pour un rang donné */
export function partenairesDeRang(rang: number): number[] {
  const i = _findIndex(rang);
  return i >= 0 ? _entries[i].ps : [];
}

