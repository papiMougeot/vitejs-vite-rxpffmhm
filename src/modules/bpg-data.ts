export function infoBpgDeId(id: BpgId): BpgInfo | null {
  const [b, p, g] = id.split('-');
  const bi = BASSINS.indexOf(b);
  if (bi < 0) return null;
  const i = bi * 140 + (parseInt(p) - 1) * 10 + (parseInt(g) - 1);
  return i >= 0 && i < 1960 ? _indexToInfo(i) : null;
}

/** Toutes les galeries d'un bassin */
export function galeriesBassin(bassin: string): BpgInfo[] {
  const bi = BASSINS.indexOf(bassin);
  if (bi < 0) return [];
  return Array.from({length: 140}, (_, j) => _indexToInfo(bi * 140 + j));
}

/** Toutes les galeries d'un puits */
export function galeriesPuits(bassin: string, puits: number): BpgInfo[] {
  const bi = BASSINS.indexOf(bassin);
  if (bi < 0) return [];
  const base = bi * 140 + (puits - 1) * 10;
  return Array.from({length: 10}, (_, j) => _indexToInfo(base + j));
}

