// ── Types ────────────────────────────────────────────────────────
export type BpgId = string;  // format 'B-P-G' ex: 'C-11-1'

export interface BpgInfo {
  bassin:        string;   // A à N
  puits:         number;   // 1 à 14
  galerie:       number;   // 1 à 10
  index:         number;   // 0 à 1959
  rangDebut:     number;
  rangFin:       number;
  partenaires:   number[]; // préfixe commun (0, 1, 2 ou 3 numéros)
  nbPartenaires: number;
}

// ── Constantes ───────────────────────────────────────────────────
const BASSINS = ['A','B','C','D','E','F','G','H','I','J','K','L','M','N'];

// ── Parsing unique au chargement du module ────────────────────────
interface _Entry { rf: number; rd: number; ps: number[]; }

const _entries: _Entry[] = (() => {
  const raw = _RAW.split('|');
  const out: _Entry[] = [];
  let prevRf = 0;
  for (const e of raw) {
    const parts = e.split(',').map(Number);
    const rf = parts[0];
    out.push({ rf, rd: prevRf + 1, ps: parts.slice(1) });
    prevRf = rf;
  }
  return out;
})();

// ── Helpers index → coordonnées BPG ──────────────────────────────
function _indexToId(i: number): BpgId {
  const b = BASSINS[Math.floor(i / 140)];
  const p = Math.floor((i % 140) / 10) + 1;
  const g = (i % 10) + 1;
  return `${b}-${p}-${g}`;
}

function _indexToInfo(i: number): BpgInfo {
  const e = _entries[i];
  const b = BASSINS[Math.floor(i / 140)];
  const p = Math.floor((i % 140) / 10) + 1;
  const g = (i % 10) + 1;
  return {
    bassin: b, puits: p, galerie: g, index: i,
    rangDebut: e.rd, rangFin: e.rf,
    partenaires: e.ps, nbPartenaires: e.ps.length,
  };
}

// ── Recherche dichotomique rang → index ──────────────────────────
function _findIndex(rang: number): number {
  let lo = 0, hi = _entries.length - 1;
  while (lo <= hi) {
    const mid = (lo + hi) >> 1;
    const e   = _entries[mid];
    if (rang >= e.rd && rang <= e.rf) return mid;
    if (rang < e.rd) hi = mid - 1; else lo = mid + 1;
  }
  return -1;
}

