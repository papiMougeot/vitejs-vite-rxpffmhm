/**
 * Distance circulaire entre deux numéros (ressort soudé).
 * distCirculaire(1, 50) = 1  (voisins !)
 * distCirculaire(1, 26) = 25 (antipodaux)
 * distCirculaire(1, 25) = 24
 */
export function distCirculaire(a: number, b: number): number {
  const d = Math.abs(a - b);
  return Math.min(d, 50 - d);
}

/**
 * Point antipodal d'un numéro (côté diamétralement opposé du ressort).
 * antipodal(1) = 26, antipodal(25) = 50, antipodal(26) = 1
 */
export function antipodal(n: number): number {
  return ((n - 1 + 25) % 50) + 1;
}

/**
 * Les k voisins immédiats de n dans la topologie circulaire.
 * voisinsCirculaires(1, 2) = [50, 2]
 * voisinsCirculaires(50, 3) = [47, 48, 49, 1, 2, 3]
 */
export function voisinsCirculaires(n: number, k: number = 1): number[] {
  const res: number[] = [];
  for (let i = k; i >= 1; i--) res.push((n - 1 - i + 50) % 50 + 1);
  for (let i = 1; i <= k; i++) res.push((n - 1 + i) % 50 + 1);
  return res;
}

/**
 * Profil dizaines d'une combinaison — ex: [1,3,5]
 */
export function profilDizaines(boules: number[]): number[] {
  return [...new Set(boules.map(n => Math.ceil(n / 10)))].sort((a,b) => a-b);
}

/**
 * Clé de profil dizaines — ex: '1-3-5'
 */
export function cleProfilDizaines(boules: number[]): string {
  return profilDizaines(boules).join('-');
}

/**
 * Membres d'une dizaine (1 à 5)
 */
export function membresDizaine(d: 1|2|3|4|5): number[] {
  return Array.from({length: 10}, (_, i) => (d-1)*10 + i + 1);
}

/**
 * Membres d'une finale (racine numérique, 1 à 9)
 */
export function membresFinale(f: number): number[] {
  const res: number[] = [];
  for (let n = 1; n <= 50; n++) {
    let x = n;
    while (x >= 10) { let s = 0, t = x; while (t > 0) { s += t % 10; t = Math.floor(t/10); } x = s; }
    if (x === f) res.push(n);
  }
  return res;
}

/**
 * Membres d'une terminaison (mod 10, 0 à 9)
 */
export function membresTerminaison(t: number): number[] {
  return Array.from({length: 50}, (_, i) => i + 1).filter(n => n % 10 === t);
}

